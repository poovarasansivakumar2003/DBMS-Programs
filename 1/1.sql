-- 1. Retrieve details of all books in the library â€“ id, title, name of publisher, authors, number of copies in each branch, etc.
SELECT 
    B.BOOK_ID, 
    B.TITLE, 
    B.PUBLISHER_NAME, 
    A.AUTHOR_NAME, 
    C.NO_OF_COPIES, 
    L.BRANCH_ID, 
    L.BRANCH_NAME, 
    L.ADDRESS
FROM BOOK B
JOIN BOOK_AUTHORS A ON B.BOOK_ID = A.BOOK_ID
JOIN BOOK_COPIES C ON B.BOOK_ID = C.BOOK_ID
JOIN LIBRARY_BRANCH L ON C.BRANCH_ID = L.BRANCH_ID;

-- 2. Get the particulars of borrowers who have borrowed more than 3 books from Jan 2017 to Jun 2017.
SELECT CARD_NO 
FROM BOOK_LENDING
WHERE DATE_OUT BETWEEN TO_DATE('01-JAN-2017', 'DD-MON-YYYY') 
                    AND TO_DATE('30-JUN-2017', 'DD-MON-YYYY')
GROUP BY CARD_NO
HAVING COUNT(*) > 3;

