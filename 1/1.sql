-- 4. Partition the BOOK table based on year of publication.
-- Creating a partitioned table (only possible at table creation)
CREATE TABLE BOOK_PARTITIONED (
    BOOK_ID INTEGER PRIMARY KEY,
    TITLE VARCHAR2(20),
    PUB_YEAR INTEGER,
    PUBLISHER_NAME VARCHAR2(20),
    CONSTRAINT FK_PUBLISHER FOREIGN KEY (PUBLISHER_NAME) REFERENCES PUBLISHER(NAME) ON DELETE CASCADE
)
PARTITION BY RANGE (PUB_YEAR) (
    PARTITION P1 VALUES LESS THAN (2010),
    PARTITION P2 VALUES LESS THAN (2015),
    PARTITION P3 VALUES LESS THAN (2020),
    PARTITION P4 VALUES LESS THAN (2030)
);

-- Demonstrate partitioned query (fetching books published after 2015)
SELECT * FROM BOOK_PARTITIONED WHERE PUB_YEAR >= 2015;

-- 5. Create a view of all books and their number of copies currently available in the Library.
CREATE VIEW V_BOOKS_AVAILABLE AS
SELECT 
    B.BOOK_ID, 
    B.TITLE, 
    SUM(C.NO_OF_COPIES) AS TOTAL_COPIES
FROM BOOK B
JOIN BOOK_COPIES C ON B.BOOK_ID = C.BOOK_ID
GROUP BY B.BOOK_ID, B.TITLE;
